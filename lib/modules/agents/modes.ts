// ─────────────────────────────────────────────────────────────────────────────
// أوضاع «اسأل حكيم» — عقل واحد (وكيل الأنظمة) + تعليمة وضع تغيّر زاوية الإخراج فقط.
// بيانات نقيّة (بلا استيراد خادميّ) كي تستوردها الواجهة (العميل) والروت (الخادم) معًا.
// التوحيد المعماريّ يبدأ بوضعين؛ تُضاف البقية بإضافة إدخالٍ هنا لا صفحةٍ جديدة.
// ─────────────────────────────────────────────────────────────────────────────
export type AgentModeId = "ask" | "analyze-case";

export interface AgentMode {
  id: AgentModeId;
  /** اسم العرض في شريط الأوضاع. */
  name: string;
  /** أيقونة (إيموجي) للعرض. */
  icon: string;
  /** تلميح قصير. */
  hint: string;
  /**
   * تعليمة النظام التي تحدّد زاوية الإخراج فوق مواد النواة المُتحقَّقة.
   * null = السلوك الافتراضي لـ«اسأل حكيم» (صياغة الاستشارة القائمة) — بلا تغيير.
   */
  systemPrompt: string | null;
}

/** تعليمة وضع «حلّل قضية»: تحليل قضية منظّم بعناوين، مؤصَّل حصريًا بمواد النواة المرفقة. */
const ANALYZE_CASE_PROMPT = [
  "أنت محلّل قضايا قانوني سعودي منضبط بالمصادر داخل منصة حكيم.",
  "حلّل القضية اعتمادًا على «المواد المرفقة من النواة» حصرًا، ولا تخترع مادة أو رقم مادة غير وارد فيها.",
  "أخرِج تحليلًا عربيًّا منظّمًا بعناوين Markdown بهذا الترتيب:",
  "## توصيف النزاع",
  "## الوقائع المنتِجة",
  "## المواد النظامية الحاكمة",
  "## عبء الإثبات",
  "## الدفوع المحتملة",
  "## نقاط القوة",
  "## نقاط الضعف",
  "## تقدير مبدئي (غير قطعيّ)",
  "إن لم تكفِ المواد لعنصرٍ فاذكر ذلك صراحةً بدل الاختلاق. اختم بتنبيه مهنيّ موجز.",
].join("\n");

export const AGENT_MODES: AgentMode[] = [
  { id: "ask", name: "اسأل", icon: "✦", hint: "بحث وإجابة مؤصّلة من النواة", systemPrompt: null },
  { id: "analyze-case", name: "حلّل قضية", icon: "🔍", hint: "تحليل قضية مؤصّل بفهم النظام الحاكم", systemPrompt: ANALYZE_CASE_PROMPT },
];

export const DEFAULT_MODE_ID: AgentModeId = "ask";

/** يحلّ معرّف الوضع إلى تعريفه؛ يسقط للوضع الافتراضي «اسأل» عند غياب/جهل المعرّف. */
export function getAgentMode(id?: string | null): AgentMode {
  return AGENT_MODES.find((m) => m.id === id) ?? AGENT_MODES[0];
}
