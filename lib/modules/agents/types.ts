/** نيّة المستخدم من مدخله — تُحدَّد قبل أي بحث (بوّابة النيّة، المرحلة ١). */
export type IntentType =
  | "greeting" // تحية
  | "thanks" // شكر
  | "meta" // سؤال عن حكيم نفسه/قدراته
  | "legal_question" // سؤال قانوني حقيقي → يمرّ للبحث
  | "ambiguous" // غير واضح → استيضاح
  | "non_legal"; // خارج النطاق القانوني → اعتذار

export interface IntentResult {
  type: IntentType;
  /** ثقة التصنيف [0..1] — تُعينُ المنسّق على اختيار المسار. */
  confidence: number;
  /** ردّ جاهز للحالات غير القانونية (تحية/شكر/meta/non_legal/ambiguous) — بلا بحث. */
  reply?: string;
  /** مصدر القرار (شفافية): deterministic أو model. */
  source: "deterministic" | "model";
}

/** نتيجة أداة موحّدة — كل أدوات المرحلة ٢ تُرجعها (بيانات + مصدر + ثقة). */
export interface ToolResult<T = unknown> {
  ok: boolean;
  data: T;
  /** مصدر الحقيقة (للاستشهاد/الشفافية): من أين جاءت البيانات. */
  source: string;
  /** ثقة النتيجة [0..1]. */
  confidence: number;
  /** رسالة حالة (مثل «بيانات ناقصة») بدل الفشل الصامت. */
  note?: string;
}

/** خطوة شفّافة تُبثّ للسطر المتحرّك (المرحلة ٨) — متوافقة مع بثّ NDJSON القائم في الراوت. */
export interface AgentStep {
  id: string;
  status: "running" | "done";
  label: string;
  data?: Record<string, unknown>;
}
