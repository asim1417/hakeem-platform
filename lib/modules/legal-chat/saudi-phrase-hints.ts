/**
 * يُعيد التلميحات المطابقة لرسالة المستخدم (قد تكون فارغة). للمساعدة فقط.
 */
export function matchPhraseHints(message: string): string[] {
  const n = normalizeArabic(message);
  const hits: string[] = [];
  for (const h of SAUDI_PHRASE_HINTS) {
    if (hasAny(n, h.keys) && !hits.includes(h.incidentType)) hits.push(h.incidentType);
  }
  return hits;
}

/**
 * يبني سطر سياقٍ مساعد يُحقن في userPrompt للنموذج (لا في system، ولا كقرار).
 * يُعيد "" إن لم تتطابق أي عبارة، فلا يُضاف ضجيج.
 */
export function buildPhraseHintContext(message: string): string {
  const hits = matchPhraseHints(message);
  if (!hits.length) return "";
  return `تلميحات لهجة قد تساعدك (للاسترشاد فقط، والحكم النهائي لك بالمعنى): ${hits.join(" · ")}.`;
}
