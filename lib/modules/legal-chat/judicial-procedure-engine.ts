/** بحسب المرحلة، يضيف فحوص القبول الخاصة بالاعتراض. */
function admissibilityForStage(stage: ProceduralStage): string[] {
  const extra: string[] = [];
  if (stage === "APPEAL") extra.push("إيداع لائحة الاعتراض ضمن ميعاد الاستئناف", "بيان أسباب الاعتراض وتعلّقها بالوقائع أو التطبيق");
  if (stage === "CASSATION") extra.push("توافر أسباب النقض النظامية (مخالفة النظام/الخطأ في تطبيقه/الإخلال بالإجراء) دون إعادة تقدير الموضوع");
  if (stage === "RECONSIDERATION") extra.push("توافر أحد أسباب التماس إعادة النظر الحصرية");
  return extra;
}

/** الدفوع الشكلية/الإجرائية التي يرجّح نظرها أولاً قبل الموضوع. */
function defensesHeardFirst(track: LegalTrack): string[] {
  const base = [
    "الدفع بعدم الاختصاص (نوعي/مكاني/ولائي)",
    "الدفع بعدم قبول الدعوى (انتفاء الصفة/المصلحة/فوات الميعاد)",
    "الدفع بسبق الفصل أو وجود شرط تحكيم",
  ];
  if (track === "CRIMINAL") base.push("الدفع ببطلان إجراءات الضبط/التفتيش/القبض");
  if (track === "ARBITRATION") base.push("الدفع بعدم اختصاص هيئة التحكيم أو بطلان شرط التحكيم");
  return base;
}

/** إجراءات المرحلة الحالية. */
function stageActions(stage: ProceduralStage, track: LegalTrack): string[] {
  switch (stage) {
    case "PRE_LITIGATION":
      return ["تقدير المركز القانوني قبل التقاضي", "التحقق من الشروط السابقة (مطالبة/إنذار) إن لزمت", "جمع المستندات والبيّنات"];
    case "FILING":
      return ["تحرير صحيفة الدعوى وتحديد الطلبات", "إرفاق المستندات والوكالة", "التحقق من الاختصاص قبل القيد"];
    case "FIRST_INSTANCE":
      return ["تبادل المذكرات", "تحرير محل النزاع", "تحديد الوقائع محل الإثبات"];
    case "EVIDENCE":
      return ["تقديم البيّنات ومناقشتها", "طلب الخبرة/الشهادة/الاستجواب عند الحاجة", "بحث قبول الدليل وإنتاجه"];
    case "PLEADING_CLOSED":
      return ["مراجعة اكتمال البيّنة قبل الحكم", "تحرير المسائل محل الفصل"];
    case "JUDGMENT_ISSUED":
    case "APPEAL":
    case "CASSATION":
    case "RECONSIDERATION":
      return ["استخراج منطوق الحكم وأسبابه", "تحديد مواضع الخطأ (نظامي/إجرائي/إثباتي)", "تحرير أسباب الاعتراض والطلبات"];
    case "EXECUTION":
      return ["التحقق من السند التنفيذي", "بحث أوجه منازعة التنفيذ"];
    default:
      return ["تحديد المرحلة الإجرائية لضبط الإجراءات اللاحقة"];
  }
}

/** ملاحظات مسار خاصة (تمييز المسارات عن بعضها). */
function trackNotes(track: LegalTrack): string[] {
  switch (track) {
    case "CRIMINAL":
      return ["لا يُعامل المسار الجزائي كدعوى مدنية: ميّز بين الحق العام والحق الخاص، والإجراءات (استدلال/تحقيق/محاكمة)، ومشروعية الدليل."];
    case "ARBITRATION":
      return ["يبدأ التحليل من اتفاق التحكيم ونطاقه وتشكيل الهيئة والاختصاص، لا من اختصاص القضاء العام."];
    case "COMMERCIAL":
      return ["راعِ خصوصية الإثبات التجاري: الفواتير، كشوف الحساب، المراسلات، الدليل الرقمي، والعرف التجاري، والخبرة المحاسبية."];
    case "LABOR":
      return ["راعِ قواعد عبء الإثبات الخاصة بعلاقة العمل، والتسوية الودّية قبل الدعوى."];
    default:
      return [];
  }
}

/** يبني خريطة الإجراء الكاملة من النيّة. */
export function buildProcedureMap(intent: IntentResult): ProcedureMap {
  const track = intent.track;
  const stage = intent.proceduralStage;

  // قلّم خريطة المسار العام بحسب المرحلة (الاعتراض يركّز على ما بعد الحكم).
  let roadmap = GENERAL_ROADMAP;
  if (stage === "APPEAL" || stage === "CASSATION" || stage === "RECONSIDERATION" || intent.hasJudgment) {
    roadmap = GENERAL_ROADMAP.slice(GENERAL_ROADMAP.indexOf("إصدار الحكم وتسبيبه ومنطوقه"));
  } else if (stage === "PRE_LITIGATION") {
    roadmap = GENERAL_ROADMAP.slice(0, 4);
  }

  const relevantSystems = [...TRACK_SYSTEMS[track]];
  if ((intent.hasArbitrationClause || track === "ARBITRATION") && !relevantSystems.includes("نظام التحكيم")) {
    relevantSystems.unshift("نظام التحكيم");
  }
  if ((stage === "APPEAL" || stage === "CASSATION") && !relevantSystems.includes("لائحة طرق الاعتراض على الأحكام")) {
    relevantSystems.push("لائحة طرق الاعتراض على الأحكام");
  }

  return {
    relevantSystems,
    jurisdictionQuestions: JURISDICTION_QUESTIONS[track],
    admissibilityChecks: [...BASE_ADMISSIBILITY, ...admissibilityForStage(stage)],
    proceduralRoadmap: roadmap,
    defensesHeardFirst: defensesHeardFirst(track),
    stageActions: stageActions(stage, track),
    notes: trackNotes(track),
  };
}
