"""
format_minutes.py
مُنسِّق بنيوي لمحاضر ضبط الجلسات والأحكام والصكوك:
يقسّم النص (بعد تصحيح اتجاهه) إلى أقسام واضحة ومعنونة:
  === [ترويسة / كليشة وزارة العدل] ===   (المملكة، وزارة العدل، رقم الصك، الصفحة...)
  === [بيانات الأطراف] ===               (المدعي/المدعى عليه/الحاضرون/الهوية/الصفة...)
  === [نص الضبط/الحكم] ===               (المتن الفعلي: الوقائع، الأسباب، المنطوق...)
يفصل أيضاً حدود الصفحات. يُستخدم من make_word لإنتاج مستندات Word مقروءة ومنظّمة.

الاستخدام كوحدة: from format_minutes import structure_text
أو مستقلاً: python3 format_minutes.py <ملف نصي>
"""
import re

# كلمات الترويسة/الكليشة (boilerplate رسمي يتكرر بلا قيمة موضوعية)
LETTERHEAD = [
    "المملكة العربية السعودية", "وزارة العدل", "نظام ادارة الدعاوى", "نظام إدارة الدعاوى",
    "تقارير", "رقم الصك", "تاريخ الصك", "رقم الصفحة", "الرقم المرجعي", "الكود المرجعي",
    "البوابة", "القضاء التجاري", "الخدمات الالكترونية", "الصفحة :", "ص.ب", "الرمز البريدي",
    "البريد الالكتروني", "البريد الإلكتروني", "الهاتف", "هاتف", "جوال", "www", "@", "gmail",
    "الحمد لله وحده", "صفحة",
]
# كلمات جدول بيانات الأطراف
PARTIES = [
    "المدعي", "المدعى عليه", "المدعى عليها", "الحاضرون", "الحاضر", "الصفة", "رقم الهوية",
    "هوية وطنية", "الهوية الوطنية", "الجنسية", "سعودي", "سعوديه", "الوكيل", "ممثل المدعي",
    "ممثل", "المهنة", "مكان العمل", "رقم السجل", "بيانات المدعي", "بيانات المدعى",
    "مقدم الطلب", "اسم", "العنوان", "رقم الجوال", "أصالة عن نفسه", "وكالة",
]
# مؤشرات بداية المتن (الوقائع/المنطوق/سير الجلسة/الشكوى)
BODY_START = [
    "الوقائع", "تتلخص", "وبعد", "بعد الاطلاع", "بعد سماع", "وبعرض", "تقرر", "حكمت",
    "قررت", "نص الحكم", "منطوق", "افتتحت الجلسة", "افتتح", "حضر في هذه الجلسة", "أفاد",
    "قرر المدعي", "قرر المدعى", "بناء على", "بناءً على", "وحيث", "أسباب", "الأسباب",
    "لذلك حكمت", "فلدى", "وعليه", "الموضوع", "السلام عليكم",
]
# مؤشرات قسم الطلبات/المرفقات (في الشكاوى والمذكرات)
REQUESTS = [
    "المرفقات", "مرفقات", "الطلبات", "نلتمس", "ألتمس", "نطلب من", "أطلب من",
    "لذا نلتمس", "لذا أطلب", "بناء على ما سبق", "بناءً على ما سبق", "هذه الأسباب",
    "الخلاصة", "خلاصة", "مقدمه", "مقدّمه", "صورة من", "مرفق رقم", "مرفق (",
]
# مؤشرات المُرسَل إليه/المخاطَب (في الشكاوى والخطابات)
RECIPIENT = [
    "معالي", "صاحب الفضيلة", "أصحاب الفضيلة", "وزير العدل", "ولي العهد", "رئيس",
    "المحترم", "حفظه الله", "سلمه الله", "سلمهم الله", "وكيل الوزارة", "المجلس الأعلى",
]

PAGE = re.compile(r"^\s*\[?\s*صفحة\s*\d+", )


def _score(line, kws):
    return sum(1 for k in kws if k in line)


def _classify(line):
    s = line.strip()
    if PAGE.match(s) or re.match(r"^\[صفحة", s):
        return "PAGE"
    if any(b in s for b in REQUESTS):
        return "REQUESTS"
    if any(b in s for b in BODY_START) and len(s) > 20:
        return "BODY"
    if _score(s, RECIPIENT) > 0 and len(s) < 110:
        return "RECIPIENT"
    lh = _score(s, LETTERHEAD)
    pa = _score(s, PARTIES)
    if lh > pa and lh > 0:
        return "LETTERHEAD"
    if pa > 0 and len(s) < 80:
        return "PARTIES"
    return "BODY"


LABELS = {
    "LETTERHEAD": "=== [ترويسة / كليشة رسمية — معلومات متكررة] ===",
    "RECIPIENT": "=== [المُرسَل إليه / المخاطَب والتحية] ===",
    "PARTIES": "=== [بيانات الأطراف: المدعي / المدعى عليه / الحاضرون] ===",
    "BODY": "=== [المتن الفعلي: الوقائع / الأسباب / المنطوق] ===",
    "REQUESTS": "=== [الطلبات / المرفقات] ===",
}


def structure_text(text):
    """يعيد النص مقسّماً بعناوين أقسام واضحة، مع الحفاظ على ترتيب القراءة."""
    if not text:
        return text
    out = []
    prev = None
    for raw in text.split("\n"):
        line = raw.rstrip()
        if not line.strip():
            out.append("")
            continue
        cat = _classify(line)
        if cat == "PAGE":
            out.append("")
            out.append("──────────────  " + line.strip().strip("[]") + "  ──────────────")
            prev = None
            continue
        if cat != prev:
            out.append("")
            out.append(LABELS[cat])
            prev = cat
        out.append(line)
    return "\n".join(out)


if __name__ == "__main__":
    import sys
    p = sys.argv[1]
    print(structure_text(open(p, encoding="utf-8").read()))
