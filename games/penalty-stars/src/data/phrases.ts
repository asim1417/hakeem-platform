// رسائل شاشة النتيجة حسب عدد الأهداف من ٥
export function resultMessage(goals: number): string {
  if (goals >= 5) return 'أسطورة الملعب! 🏆';
  if (goals === 4) return 'لاعب عالمي! 🌍';
  if (goals === 3) return 'أنت نجم البلنتيات! ⭐';
  if (goals === 2) return 'ممتاز يا نجم! ✨';
  return 'محاولة جميلة يا بطل، خلنا نجرب مرة ثانية! 💪';
}
